a =int(input("Enter the first number: "))
b =int(input("Enter the secind number: "))
symbol =input("Enter the symbol which should be +, -, *, / or %: ")
if (symbol=="+"):
    print(a+b)
elif(symbol=="-"):
    print(a-b)
elif(symbol=="*"):
    print(a*b)
elif(symbol=="a/b"):
    print(a/b)
elif(symbol=="a%b"):
    print(a%b)